import math
ang = float(input('Digite o 창ngulo que vc deseja: '))
print(f'O 창ngulo de {ang} tem o SENO de {math.sin(math.radians(ang)):.2f}')
print(f'O 창ngulo de {ang} tem o COSSENO de {math.cos(math.radians(ang)):.2f}')
print(f'O 창ngulo de {ang} tem a TANGENTE de {math.tan(math.radians(ang)):.2f}')
